     * Return all users of specific dealer.
     *
     * @return \Illuminate\Http\Response
     */
    public function index($id)
    {
        $dealer = Dealer::with('users')->findOrFail($id);
        $currentUser = auth()->user();

        abort_unless($dealer->isVisibleTo($currentUser), 404);

        return response($dealer);
    }

